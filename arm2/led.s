		AREA |.text|, CODE, READONLY, ALIGN=2
        EXPORT L_LED_ON
		EXPORT R_LED_ON

GPIO_PORTF_BASE EQU 0x40025000
PIN_RIGHT       EQU 0x10
PIN_LEFT        EQU 0x20

L_LED_ON
		LDR     R0, =GPIO_PORTF_BASE
		MOV     R1, #PIN_LEFT      
		STR     R1, [R0, #0x3FC]
		NOP
		NOP
		NOP
		BX      LR
R_LED_ON
		LDR     R0, =GPIO_PORTF_BASE
		MOV     R1, #PIN_RIGHT       
		STR     R1, [R0, #0x3FC]
		NOP
		NOP
		NOP
		BX      LR